package ch01;
public class Calculator{
	
	//Method -> function (재료>처리>반환)
	//반환타입 메소드이름(매개변수들) {처리구문}
	void plus(int a, int b) {
		System.out.println(a+b);
	}
	void minus(int a, int b) {
		System.out.println(a-b);
	}
	void multi(int a, int b) {
		System.out.println(a*b);
	}
	void div(int a, int b) {
		System.out.println(a/b);
	}
}
